package com.leetcode;

import java.util.HashSet;

public class ValidSudoku {
    //char[][]b = {{'5', '3', '.', '.', '7', '.', '.', '.', '.'}};
    static boolean Sudoku(String[][] board) {
        HashSet seen = new HashSet();
        for (int i = 0; i < 9; ++i) {
            for (int j = 0; j < 9; ++j) {
                if (board[i][j] != ".") {
                    String b = "(" + board[i][j] + ")";
                    if (!seen.add(b + " in row " + i) ||
                            !seen.add(b + " in column " + j) ||
                            !seen.add(b + " in block " + i / 3 + "-" + j / 3)) {
                        return false;

                    }

                }
            }
        }
        return true;

    }

    public static void main(String[] args) {
        String[][] board = {{"5", "3", ".", ".", "7", ".", ".", ".", "."}, {"6", ".", ".", "1", "9", "5", ".", ".", "."}
                , {".", "9", "8", ".", ".", ".", ".", "6", "."}, {"8", ".", ".", ".", "6", ".", ".", ".", "3"}, {"4", ".", ".", "8", ".", "3", ".", ".", "1"}
                , {"7", ".", ".", ".", "2", ".", ".", ".", "6"}, {".", "6", ".", ".", ".", ".", "2", "8", "."},
                {".", ".", ".", "4", "1", "9", ".", ".", "5"}, {".", ".", ".", ".", "8", ".", ".", "7", "9"}};
        String[][] board1 = {{"8", "3", ".", ".", "7", ".", ".", ".", "."},
                {"6", ".", ".", "1", "9", "5", ".", ".", "."},
                {".", "9", "8", ".", ".", ".", ".", "6", "."},
                {"8", ".", ".", ".", "6", ".", ".", ".", "3"},
                {"4", ".", ".", "8", ".", "3", ".", ".", "1"},
                {"7", ".", ".", ".", "2", ".", ".", ".", "6"},
                {".", "6", ".", ".", ".", ".", "2", "8", "."},
                {".", ".", ".", "4", "1", "9", ".", ".", "5"},
                {".", ".", ".", ".", "8", ".", ".", "7", "9"}};
        System.out.println(Sudoku(board));
        System.out.println(Sudoku(board1));

    }
}
